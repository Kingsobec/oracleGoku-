function displayteamDetails(teamDetails) {
  let displayTeam = teamDetails.map(function (item) {
    // console.log(item)
    return `<div class="team-member">
              <img src="${item.img}" alt="">
              <p class="team-name">${item.name}</p>
              <p class="team-desc">${item.description}</p>
            </div>`;
  });
  displayTeam = displayTeam.join("");
  teamSection.innerHTML = displayTeam;
}

function displayNftDetails(nftDetails) {
  let displayNft = nftDetails.map(function (item) {
    return `<div class="nft">
              <img src="${item.img}" alt="">
            </div>`;
  });
  displayNft = displayNft.join("");
  nftSection.innerHTML = displayNft;
}

