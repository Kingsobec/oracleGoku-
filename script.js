selector.forEach(function (question) {
  const answer = question.querySelector(".shower");
  const icons = question.querySelector("i");
  answer.addEventListener("click", function () {
    selector.forEach(function (oracle) {
      if (oracle !== question) {
        oracle.classList.remove("show-answer");
        const icon = oracle.querySelector("i");
        icon.classList.remove("fa-caret-up");
      }
    });
    icons.classList.toggle("fa-caret-up");
    question.classList.toggle("show-answer");
  });
});

